#!/usr/bin/env python3
"""Render audio using the same sample-playback strategy as Ableton Learning Music.

This reproduces the mechanism used by the "The playground" page:
1. Drum sounds are one-shot samples loaded through Tone.Players.
2. Tonal sounds are sparse keymaps loaded through Tone.Sampler.
3. Missing pitches are generated by picking the nearest mapped sample and
   changing playback rate by semitone ratio.
4. Tonal voices use instrument-specific release tails.
5. Drum choke rules (for example closed hat cutting open hat) are respected.

The script intentionally stays dependency-free and delegates decoding,
pitch-shifting and mixing to ffmpeg/ffprobe.
"""

from __future__ import annotations

import argparse
import json
import math
import shutil
import subprocess
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Optional, Sequence, Tuple


